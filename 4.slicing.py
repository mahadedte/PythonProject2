# sequence[start:stop:step]
# start: index to start (inclusive)
# stop: index to stop (exclusive)
# step: how many steps to move (default is 1)

name = "mahade"
print(name[:-1])
# mahad

name = "mahade"
print(name[:-2])
# maha

name = "mahade"
print(name[-4:])
# hade

name = "mahade"
print(name[-4:-1])
# had

print(name[::-1])
# edaham

print(name[::2])
# mhd
